package org.example;

public class zad7 {
    public static void main(String[] args) {
        printstars(5);
        printstars(3);
        printstars(9);


    }
    public static void printstars(int read) {
        for(int i=0;i<read;i++) {
            System.out.print("*");


        }
        System.out.println(" ");

    }
}
